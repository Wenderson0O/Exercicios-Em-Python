# Operações Aritméticas: + Adição, - Subtração, * Multiplicação, / Divisão, ** Potência,
# // Divisão inteira, % Resto da Divisão.
#Ordem de Precedência: [1 (), 2 **, 3 *, /, //, %, 4 +, -].
